using System;
using System.Collections.Generic;
using System.Globalization;
using System.Text;
using SESpriteLCDLayoutTool.Models;
using SESpriteLCDLayoutTool.Models.Rig;

namespace SESpriteLCDLayoutTool.Services
{
    /// <summary>
    /// Phase B runtime code generator for 2D rig animation.
    /// Produces a self-contained, paste-ready C# block that contains:
    ///   • baked rig data (origin, bones, bindings, clip keyframes)
    ///   • a small runtime sampler/evaluator that mirrors <see cref="RigClipSampler"/>
    ///     and <see cref="RigEvaluator"/> exactly, with no external references
    ///   • a DrawRig(IMyTextSurface, float time) method that draws every bound
    ///     sprite at its animated pose plus every unbound sprite at its rest pose
    ///
    /// The output uses only Sandbox.ModAPI / VRage.Game.GUI.TextPanel / VRageMath types
    /// so it pastes into PB scripts, mods, plugins, or Pulsar plugins unchanged.
    /// Unbound sprites are emitted at their literal layout positions (same shape
    /// the regular <see cref="CodeGenerator"/> emits) so the result matches the
    /// editor preview when <c>time = 0</c>.
    /// </summary>
    public static class RigCodeGenerator
    {
        private static readonly CultureInfo Inv = CultureInfo.InvariantCulture;

        /// <summary>
        /// Generates a complete runtime rig+animation snippet for the first enabled
        /// rig on the layout. Returns a comment-only stub if there is no rig.
        /// </summary>
        public static string Generate(LcdLayout layout, string methodName = "DrawRig", string surfaceParam = "surface")
        {
            if (layout == null) throw new ArgumentNullException(nameof(layout));
            var rig = PickRig(layout);
            var sb = new StringBuilder();

            sb.AppendLine("// ─── Generated by SE Sprite LCD Layout Tool — Rig Runtime ───");
            sb.AppendLine($"// Generated: {DateTime.Now:yyyy-MM-dd HH:mm}");
            sb.AppendLine("// Required usings:");
            sb.AppendLine("//   using System.Collections.Generic;");
            sb.AppendLine("//   using VRage.Game.GUI.TextPanel;   // MySprite, SpriteType, ContentType");
            sb.AppendLine("//   using VRageMath;                  // Vector2, Color");
            sb.AppendLine("//   using Sandbox.ModAPI;             // IMyTextSurface  (or Sandbox.ModAPI.Ingame for PB)");
            sb.AppendLine("//");
            sb.AppendLine($"// Call {methodName}({surfaceParam}, t) every tick where t advances in seconds.");
            sb.AppendLine("// — OR — call AddRigSprites(frame, _rigTime); from inside an existing");
            sb.AppendLine("// using(var frame = surface.DrawFrame()) block to compose with static sprites.");
            sb.AppendLine("// _rigTime is wall-clock-derived, so playback is identical whether the host");
            sb.AppendLine("// calls render once per tick or only intermittently (e.g. editor preview).");
            sb.AppendLine();

            if (rig == null || rig.Bones == null || rig.Bones.Count == 0)
            {
                sb.AppendLine("// No rig present on this layout — nothing to emit.");
                sb.AppendLine($"public void {methodName}(IMyTextSurface {surfaceParam}, float time) {{ }}");
                return sb.ToString();
            }

            EmitDataTypes(sb);
            EmitDataLiterals(sb, layout, rig);
            EmitSamplerAndEvaluator(sb);
            EmitTimeField(sb);
            EmitAddRigSpritesMethod(sb, layout, rig);
            EmitDrawMethod(sb, layout, rig, methodName, surfaceParam);

            return sb.ToString();
        }

        /// <summary>
        /// Returns the GUIDs (<see cref="SpriteEntry.Id"/>) of every sprite that the
        /// active rig binds to a bone. Used by <see cref="RigCodeInjector"/> to mute
        /// the corresponding static <c>frame.Add(...)</c> blocks so they don't double-render.
        /// </summary>
        public static System.Collections.Generic.HashSet<string> GetBoundSpriteIds(LcdLayout layout)
        {
            var result = new System.Collections.Generic.HashSet<string>(StringComparer.Ordinal);
            if (layout == null) return result;
            var rig = PickRig(layout);
            if (rig == null || rig.Bindings == null || layout.Sprites == null) return result;
            foreach (var bd in rig.Bindings)
            {
                if (bd == null || bd.Muted || bd.SpriteIndex < 0 || bd.SpriteIndex >= layout.Sprites.Count) continue;
                var sp = layout.Sprites[bd.SpriteIndex];
                if (sp != null && !string.IsNullOrEmpty(sp.Id)) result.Add(sp.Id);
            }
            return result;
        }

        private static Rig PickRig(LcdLayout layout)
        {
            if (layout.Rigs == null) return null;
            foreach (var r in layout.Rigs)
                if (r != null && r.Enabled && r.Bones != null && r.Bones.Count > 0) return r;
            // Fall back to the first rig even if disabled, so the user still sees output.
            foreach (var r in layout.Rigs) if (r != null) return r;
            return null;
        }

