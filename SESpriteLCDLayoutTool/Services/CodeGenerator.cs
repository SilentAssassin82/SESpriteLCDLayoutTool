using System;
using System.Text;
using SESpriteLCDLayoutTool.Models;

namespace SESpriteLCDLayoutTool.Services
{
    /// <summary>Target code style for generated output.</summary>
    public enum CodeStyle
    {
        /// <summary>Programmable Block (ingame) script — uses Sandbox.ModAPI.Ingame.</summary>
        InGame,
        /// <summary>C# mod — uses Sandbox.ModAPI (not .Ingame) with full using directives.</summary>
        Mod,
        /// <summary>Torch/Plugin — uses Sandbox.ModAPI with full using directives and session context hints.</summary>
        Plugin,
    }

    public static class CodeGenerator
    {
        /// <summary>
        /// Generates a complete C# method body that draws every sprite in the layout
        /// onto an IMyTextSurface, ready to paste into an SE plugin, mod, or ingame script.
        /// </summary>
        public static string Generate(LcdLayout layout,
                                      CodeStyle style     = CodeStyle.InGame,
                                      string methodName   = "DrawLayout",
                                      string surfaceParam = "surface")
        {
            var sb = new StringBuilder();

            sb.AppendLine($"// ─── Generated by SE Sprite LCD Layout Tool  [{DateTime.Now:yyyy-MM-dd HH:mm}] ───");
            sb.AppendLine($"// Surface: {layout.SurfaceWidth} × {layout.SurfaceHeight}  |  Sprites: {layout.Sprites.Count}");
            sb.AppendLine("//");

            switch (style)
            {
                case CodeStyle.Mod:
                    sb.AppendLine("// Required using directives:");
                    sb.AppendLine("//   using VRage.Game.GUI.TextPanel;   // SpriteType, MySprite, ContentType");
                    sb.AppendLine("//   using VRageMath;                  // Vector2, Color");
                    sb.AppendLine("//   using Sandbox.ModAPI;             // IMyTextSurface");
                    sb.AppendLine("//");
                    sb.AppendLine("// NOTE: Mod code — use IMyTextSurface from Sandbox.ModAPI (not .Ingame).");
                    break;

                case CodeStyle.Plugin:
                    sb.AppendLine("// Required using directives:");
                    sb.AppendLine("//   using VRage.Game.GUI.TextPanel;   // SpriteType, MySprite, ContentType");
                    sb.AppendLine("//   using VRageMath;                  // Vector2, Color");
                    sb.AppendLine("//   using Sandbox.ModAPI;             // IMyTextSurface");
                    sb.AppendLine("//");
                    sb.AppendLine("// NOTE: Plugin/Torch code — obtain an IMyTextSurface from a terminal block:");
                    sb.AppendLine("//   var lcd = block as IMyTextSurfaceProvider;");
                    sb.AppendLine("//   var surface = lcd?.GetSurface(0);");
                    break;

                default: // InGame / PB
                    sb.AppendLine("// Required using directives:");
                    sb.AppendLine("//   using VRage.Game.GUI.TextPanel;   // SpriteType, MySprite, ContentType");
                    sb.AppendLine("//   using VRageMath;                  // Vector2, Color");
                    sb.AppendLine("//   using Sandbox.ModAPI.Ingame;      // IMyTextSurface  (ingame scripts only)");
                    break;
            }

            sb.AppendLine("//");
            sb.AppendLine("// Sprite positions are the CENTER of each sprite in surface-local pixel coordinates.");
            sb.AppendLine("// For surfaces that do not fill the full texture (small LCDs, corner blocks) you");
            sb.AppendLine("// may need to add the viewport offset:  var ofs = (surface.TextureSize - surface.SurfaceSize) / 2f;");
            sb.AppendLine();

            string access = style == CodeStyle.InGame ? "private" : "public";
            sb.AppendLine($"{access} void {methodName}(IMyTextSurface {surfaceParam})");
            sb.AppendLine("{");
            sb.AppendLine($"    {surfaceParam}.ContentType = ContentType.SCRIPT;");
            sb.AppendLine($"    {surfaceParam}.Script = \"\";");
            sb.AppendLine();
            sb.AppendLine($"    using (var frame = {surfaceParam}.DrawFrame())");
            sb.AppendLine("    {");

            if (layout.Sprites.Count == 0)
            {
                sb.AppendLine("        // No sprites in layout — add some in the designer!");
            }
            else
            {
                for (int i = 0; i < layout.Sprites.Count; i++)
                {
                    var sp = layout.Sprites[i];
                    sb.AppendLine($"        // [{i + 1}] {sp.DisplayName}");
                    sb.AppendLine("        frame.Add(new MySprite");
                    sb.AppendLine("        {");

                    if (sp.Type == SpriteEntryType.Text)
                    {
                        sb.AppendLine($"            Type           = SpriteType.TEXT,");
                        sb.AppendLine($"            Data           = {Q(sp.Text)},");
                        if (sp.IsReferenceLayout)
                            sb.AppendLine($"            // Position  = new Vector2({sp.X:F1}f, {sp.Y:F1}f),  // snapshot reference — replace with your variable");
                        else
                            sb.AppendLine($"            Position       = new Vector2({sp.X:F1}f, {sp.Y:F1}f),");
                        sb.AppendLine($"            Color          = new Color({sp.ColorR}, {sp.ColorG}, {sp.ColorB}, {sp.ColorA}),");
                        sb.AppendLine($"            FontId         = {Q(sp.FontId)},");
                        sb.AppendLine($"            Alignment      = TextAlignment.{sp.Alignment.ToString().ToUpperInvariant()},");
                        sb.AppendLine($"            RotationOrScale = {sp.Scale:F2}f,  // scale for TEXT sprites");
                    }
                    else
                    {
                        sb.AppendLine($"            Type           = SpriteType.TEXTURE,");
                        sb.AppendLine($"            Data           = {Q(sp.SpriteName)},");
                        if (sp.IsReferenceLayout)
                        {
                            sb.AppendLine($"            // Position  = new Vector2({sp.X:F1}f, {sp.Y:F1}f),  // snapshot reference");
                            sb.AppendLine($"            // Size      = new Vector2({sp.Width:F1}f, {sp.Height:F1}f),  // snapshot reference");
                        }
                        else
                        {
                            sb.AppendLine($"            Position       = new Vector2({sp.X:F1}f, {sp.Y:F1}f),");
                            sb.AppendLine($"            Size           = new Vector2({sp.Width:F1}f, {sp.Height:F1}f),");
                        }
                        sb.AppendLine($"            Color          = new Color({sp.ColorR}, {sp.ColorG}, {sp.ColorB}, {sp.ColorA}),");
                        sb.AppendLine($"            Alignment      = TextAlignment.CENTER,");
                        sb.AppendLine($"            RotationOrScale = {sp.Rotation:F4}f,  // radians");
                    }

                    sb.AppendLine("        });");

                    if (i < layout.Sprites.Count - 1)
                        sb.AppendLine();
                }
            }

            sb.AppendLine("    }");
            sb.AppendLine("}");
            return sb.ToString();
        }

        private static string Q(string s)   => $"\"{Esc(s)}\"";

        /// <summary>
        /// Generates a C# helper method that the user can drop into their PB/mod/plugin.
        /// When called at runtime, it serializes every sprite currently on a surface
        /// into literal MySprite initializer code that the layout tool can import
        /// with exact positions.
        /// </summary>
        public static string GenerateSnapshotHelper(CodeStyle style = CodeStyle.InGame)
        {
            switch (style)
            {
                case CodeStyle.Plugin:  return GenerateSnapshotHelper_Plugin();
                case CodeStyle.Mod:     return GenerateSnapshotHelper_Mod();
                default:                return GenerateSnapshotHelper_PB();
            }
        }

        private static string GenerateSnapshotHelper_PB()
        {
            var sb = new StringBuilder();
            sb.AppendLine("// ─── LCD Snapshot Helper (Programmable Block) ───────────────────────────");
            sb.AppendLine("// Drop this into your PB script.  After your drawing code, call:");
            sb.AppendLine("//   SnapshotCollect(mySprites);");
            sb.AppendLine("//   Me.CustomData = SerializeSnapshot();");
            sb.AppendLine("// Then copy the PB's Custom Data and paste into the layout tool's");
            sb.AppendLine("// \"Paste Layout\" dialog with \"Import as reference layout\" checked.");
            sb.AppendLine("// ───────────────────────────────────────────────────────────────────────");
            sb.AppendLine();
            AppendSnapshotCore(sb, accessModifier: "");
            sb.AppendLine();
            sb.AppendLine("// ── Usage in your PB Main() ──");
            sb.AppendLine("// After your drawing loop:");
            sb.AppendLine("//   SnapshotCollect(mySprites);");
            sb.AppendLine("//   Me.CustomData = SerializeSnapshot();");
            return sb.ToString();
        }

        private static string GenerateSnapshotHelper_Mod()
        {
            var sb = new StringBuilder();
            sb.AppendLine("// ─── LCD Snapshot Helper (Mod) ─────────────────────────────────────────");
            sb.AppendLine("// Add this to your mod's session component or drawing class.");
            sb.AppendLine("// After your rendering code, call:");
            sb.AppendLine("//   SnapshotCollect(mySprites);");
            sb.AppendLine("//   var output = SerializeSnapshot();");
            sb.AppendLine("//   MyAPIGateway.Utilities.ShowMissionScreen(\"Snapshot\", \"\", \"\", output);");
            sb.AppendLine("// Copy from the mission screen and paste into the layout tool.");
            sb.AppendLine("// ───────────────────────────────────────────────────────────────────────");
            sb.AppendLine();
            sb.AppendLine("// Required usings:");
            sb.AppendLine("//   using System.Text;");
            sb.AppendLine("//   using VRage.Game.GUI.TextPanel;");
            sb.AppendLine("//   using VRageMath;");
            sb.AppendLine();
            AppendSnapshotCore(sb, accessModifier: "public ");
            return sb.ToString();
        }

        private static string GenerateSnapshotHelper_Plugin()
        {
            var sb = new StringBuilder();
            sb.AppendLine("// ─── LCD Snapshot Helper (Torch / Plugin) ──────────────────────────────");
            sb.AppendLine("// Add this to your plugin class.  Wire up SnapshotLcd() to a chat");
            sb.AppendLine("// command (e.g. !snapshot) or call it from your rendering code.");
            sb.AppendLine("//");
            sb.AppendLine("// The snapshot is written to a file and logged via NLog so you can");
            sb.AppendLine("// grab it from the Torch console or the output file.");
            sb.AppendLine("// Paste the output into the layout tool's \"Paste Layout\" dialog");
            sb.AppendLine("// with \"Import as reference layout\" checked.");
            sb.AppendLine("// ───────────────────────────────────────────────────────────────────────");
            sb.AppendLine();
            sb.AppendLine("// Required usings:");
            sb.AppendLine("//   using System.Collections.Generic;");
            sb.AppendLine("//   using System.IO;");
            sb.AppendLine("//   using System.Text;");
            sb.AppendLine("//   using NLog;");
            sb.AppendLine("//   using Sandbox.ModAPI;");
            sb.AppendLine("//   using VRage.Game.GUI.TextPanel;");
            sb.AppendLine("//   using VRageMath;");
            sb.AppendLine();
            sb.AppendLine("private static readonly Logger Log = LogManager.GetCurrentClassLogger();");
            sb.AppendLine();
            AppendSnapshotCore(sb, accessModifier: "public ");
            sb.AppendLine();
            sb.AppendLine("/// <summary>");
            sb.AppendLine("/// Snapshots all sprites currently on a surface and writes them to a file.");
            sb.AppendLine("/// Call from a chat command handler, e.g.:");
            sb.AppendLine("///   var lcd = block as IMyTextSurfaceProvider;");
            sb.AppendLine("///   SnapshotLcd(lcd?.GetSurface(0), \"MyLcdSnapshot\");");
            sb.AppendLine("/// </summary>");
            sb.AppendLine("public void SnapshotLcd(IMyTextSurface surface, string label = \"LcdSnapshot\")");
            sb.AppendLine("{");
            sb.AppendLine("    if (surface == null) { Log.Warn(\"SnapshotLcd: surface is null\"); return; }");
            sb.AppendLine();
            sb.AppendLine("    // NOTE: SE doesn't expose a way to read back an already-drawn frame.");
            sb.AppendLine("    // You need to collect sprites BEFORE adding them to the frame.");
            sb.AppendLine("    // Call SnapshotCollect(sprites) in your rendering code first.");
            sb.AppendLine("    if (_snapshotSprites.Count == 0)");
            sb.AppendLine("    {");
            sb.AppendLine("        Log.Warn(\"SnapshotLcd: no sprites collected — call SnapshotCollect() first\");");
            sb.AppendLine("        return;");
            sb.AppendLine("    }");
            sb.AppendLine();
            sb.AppendLine("    string output = SerializeSnapshot();");
            sb.AppendLine("    string path = Path.Combine(");
            sb.AppendLine("        StoragePath ?? Directory.GetCurrentDirectory(),");
            sb.AppendLine("        $\"{label}_{DateTime.Now:yyyyMMdd_HHmmss}.txt\");");
            sb.AppendLine();
            sb.AppendLine("    File.WriteAllText(path, output);");
            sb.AppendLine("    Log.Info($\"LCD snapshot saved: {path}  ({_snapshotSprites.Count} sprites)\");");
            sb.AppendLine("}");
            return sb.ToString();
        }

        /// <summary>
        /// Appends the shared SnapshotCollect + SerializeSnapshot methods.
        /// </summary>
        private static void AppendSnapshotCore(StringBuilder sb, string accessModifier)
        {
            // The snapshot collector list
            sb.AppendLine($"{accessModifier}List<MySprite> _snapshotSprites = new List<MySprite>();");
            sb.AppendLine();

            // Collect method
            sb.AppendLine("/// <summary>");
            sb.AppendLine("/// Call this with your list of MySprite objects after you've built them");
            sb.AppendLine("/// but before (or after) adding them to the frame.");
            sb.AppendLine("/// </summary>");
            sb.AppendLine($"{accessModifier}void SnapshotCollect(List<MySprite> sprites)");
            sb.AppendLine("{");
            sb.AppendLine("    _snapshotSprites.Clear();");
            sb.AppendLine("    _snapshotSprites.AddRange(sprites);");
            sb.AppendLine("}");
            sb.AppendLine();

            // Serializer
            sb.AppendLine("/// <summary>");
            sb.AppendLine("/// Serializes collected sprites into C# MySprite initializer code");
            sb.AppendLine("/// with all positions/sizes resolved to literal pixel values.");
            sb.AppendLine("/// Paste the output into the layout tool's \"Paste Layout\" dialog.");
            sb.AppendLine("/// </summary>");
            sb.AppendLine($"{accessModifier}string SerializeSnapshot()");
            sb.AppendLine("{");
            sb.AppendLine("    var sb = new StringBuilder();");
            sb.AppendLine("    sb.AppendLine(\"// ── LCD Snapshot ──\");");
            sb.AppendLine("    sb.AppendLine($\"// Captured: {DateTime.Now:yyyy-MM-dd HH:mm}  |  Sprites: {_snapshotSprites.Count}\");");
            sb.AppendLine("    sb.AppendLine();");
            sb.AppendLine("    for (int i = 0; i < _snapshotSprites.Count; i++)");
            sb.AppendLine("    {");
            sb.AppendLine("        var s = _snapshotSprites[i];");
            sb.AppendLine("        sb.AppendLine(\"frame.Add(new MySprite\");");
            sb.AppendLine("        sb.AppendLine(\"{\");");
            sb.AppendLine("        sb.AppendLine($\"    Type           = SpriteType.{s.Type},\");");
            sb.AppendLine("        sb.AppendLine($\"    Data           = \\\"{s.Data}\\\",\");");
            sb.AppendLine("        if (s.Position.HasValue)");
            sb.AppendLine("            sb.AppendLine($\"    Position       = new Vector2({s.Position.Value.X:F1}f, {s.Position.Value.Y:F1}f),\");");
            sb.AppendLine("        if (s.Size.HasValue)");
            sb.AppendLine("            sb.AppendLine($\"    Size           = new Vector2({s.Size.Value.X:F1}f, {s.Size.Value.Y:F1}f),\");");
            sb.AppendLine("        if (s.Color.HasValue)");
            sb.AppendLine("            sb.AppendLine($\"    Color          = new Color({s.Color.Value.R}, {s.Color.Value.G}, {s.Color.Value.B}, {s.Color.Value.A}),\");");
            sb.AppendLine("        if (s.FontId != null)");
            sb.AppendLine("            sb.AppendLine($\"    FontId         = \\\"{s.FontId}\\\",\");");
            sb.AppendLine("        sb.AppendLine($\"    Alignment      = TextAlignment.{s.Alignment},\");");
            sb.AppendLine("        sb.AppendLine($\"    RotationOrScale = {s.RotationOrScale:F4}f,\");");
            sb.AppendLine("        sb.AppendLine(\"});\");");
            sb.AppendLine("        sb.AppendLine();");
            sb.AppendLine("    }");
            sb.AppendLine("    return sb.ToString();");
            sb.AppendLine("}");
        }
        /// <summary>
        /// Escapes a string for a C# string literal, turning any PUA or non-ASCII
        /// character into a \uXXXX escape so the generated code is always ASCII-safe.
        /// </summary>
        private static string Esc(string s)
        {
            if (s == null) return "";
            var sb = new StringBuilder(s.Length * 2);
            foreach (char c in s)
            {
                if (c == '\\') { sb.Append("\\\\"); }
                else if (c == '"') { sb.Append("\\\""); }
                else if (c >= 0x007F)  // non-ASCII or PUA → \uXXXX
                    sb.Append($"\\u{(int)c:X4}");
                else
                    sb.Append(c);
            }
            return sb.ToString();
        }
    }
