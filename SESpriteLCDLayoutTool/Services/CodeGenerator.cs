        {
            var sb = new StringBuilder();
            sb.AppendLine("// ─── LCD Snapshot Helper (Programmable Block) ───────────────────────────");
            sb.AppendLine("// Drop this into your PB script.  After your drawing code, call:");
            sb.AppendLine("//   SnapshotCollect(mySprites);");
            sb.AppendLine("//   Me.CustomData = SerializeSnapshot();");
