            sb.AppendLine("{");
            sb.AppendLine("    if (surface == null) { Log.Warn(\"SnapshotLcd: surface is null\"); return; }");
            sb.AppendLine();
            sb.AppendLine("    // NOTE: SE doesn't expose a way to read back an already-drawn frame.");
            sb.AppendLine("    // You need to collect sprites BEFORE adding them to the frame.");
            sb.AppendLine("    // Call SnapshotCollect(sprites) in your rendering code first.");
            sb.AppendLine("    if (_snapshotSprites.Count == 0)");
            sb.AppendLine("    {");
            sb.AppendLine("        Log.Warn(\"SnapshotLcd: no sprites collected — call SnapshotCollect() first\");");
