        private static string GenerateSnapshotHelper_Plugin()
        {
            var sb = new StringBuilder();
            sb.AppendLine("// ─── LCD Snapshot Helper (Torch / Plugin) ──────────────────────────────");
            sb.AppendLine("// Add this to your plugin class.  Wire up SnapshotLcd() to a chat");
            sb.AppendLine("// command (e.g. !snapshot) or call it from your rendering code.");
            sb.AppendLine("//");
            sb.AppendLine("// The snapshot is written to a file and logged via NLog so you can");
            sb.AppendLine("// grab it from the Torch console or the output file.");
            sb.AppendLine("//");
            sb.AppendLine("// Live Streaming:  Call StartLcdStream() from a chat command (e.g.");
            sb.AppendLine("// /lcd watch) and the snippet will stream frames to the layout tool");
            sb.AppendLine("// over a named pipe for 60 seconds, then auto-disarm.  The code is");
            sb.AppendLine("// completely dormant and causes zero overhead when not streaming.");
            sb.AppendLine("//");
            sb.AppendLine("// Debug Variables:  Call SnapshotDebug(\"name\", value) before");
            sb.AppendLine("// SerializeSnapshot() to stream field values into the layout tool's");
            sb.AppendLine("// Variables tab for live debugging.");
            sb.AppendLine("// ───────────────────────────────────────────────────────────────────────");
            sb.AppendLine();
            sb.AppendLine("// Required usings:");
            sb.AppendLine("//   using System;");
            sb.AppendLine("//   using System.Collections.Generic;");
            sb.AppendLine("//   using System.IO;");
            sb.AppendLine("//   using System.IO.Pipes;");
            sb.AppendLine("//   using System.Text;");
            sb.AppendLine("//   using NLog;");
            sb.AppendLine("//   using Sandbox.ModAPI;");
            sb.AppendLine("//   using VRage.Game.GUI.TextPanel;");
            sb.AppendLine("//   using VRageMath;");
            sb.AppendLine();
            sb.AppendLine("private static readonly Logger Log = LogManager.GetCurrentClassLogger();");
            sb.AppendLine();
            AppendSnapshotCore(sb, accessModifier: "public ");
            sb.AppendLine();
            sb.AppendLine("/// <summary>");
            sb.AppendLine("/// Snapshots all sprites currently on a surface and writes them to a file.");
            sb.AppendLine("/// Call from a chat command handler, e.g.:");
            sb.AppendLine("///   var lcd = block as IMyTextSurfaceProvider;");
            sb.AppendLine("///   SnapshotLcd(lcd?.GetSurface(0), \"MyLcdSnapshot\");");
            sb.AppendLine("/// </summary>");
            sb.AppendLine("public void SnapshotLcd(IMyTextSurface surface, string label = \"LcdSnapshot\")");
            sb.AppendLine("{");
            sb.AppendLine("    if (surface == null) { Log.Warn(\"SnapshotLcd: surface is null\"); return; }");
            sb.AppendLine();
            sb.AppendLine("    // NOTE: SE doesn't expose a way to read back an already-drawn frame.");
            sb.AppendLine("    // You need to collect sprites BEFORE adding them to the frame.");
            sb.AppendLine("    // Call SnapshotCollect(sprites) in your rendering code first.");
            sb.AppendLine("    if (_snapshotSprites.Count == 0)");
            sb.AppendLine("    {");
            sb.AppendLine("        Log.Warn(\"SnapshotLcd: no sprites collected — call SnapshotCollect() first\");");
            sb.AppendLine("        return;");
            sb.AppendLine("    }");
            sb.AppendLine();
            sb.AppendLine("    string output = SerializeSnapshot();");
            sb.AppendLine("    string path = Path.Combine(");
            sb.AppendLine("        StoragePath ?? Directory.GetCurrentDirectory(),");
            sb.AppendLine("        $\"{label}_{DateTime.Now:yyyyMMdd_HHmmss}.cs\");");
            sb.AppendLine();
            sb.AppendLine("    File.WriteAllText(path, output);");
            sb.AppendLine("    Log.Info($\"LCD snapshot saved: {path}  ({_snapshotSprites.Count} sprites)\");");
            sb.AppendLine("}");
            sb.AppendLine();
            sb.AppendLine("/// <summary>");
            sb.AppendLine("/// Finds the first IMyTextSurfaceProvider block whose name or CustomData contains");
            sb.AppendLine("/// <paramref name=\"tag\"/> using a two-pass search that avoids grid-iteration false");
            sb.AppendLine("/// positives. Pass 1 checks CustomName only; Pass 2 checks CustomData only.");
            sb.AppendLine("/// This ensures a block named 'mylcd' is never returned ahead of the correct");
            sb.AppendLine("/// panel tagged 'SCREEN:Main' in its CustomData just because it appears first in");
            sb.AppendLine("/// the grid's block list.");
            sb.AppendLine("/// </summary>");
            sb.AppendLine("/// <param name=\"allBlocks\">All terminal blocks to search. Fetch once with");
            sb.AppendLine("///   GridTerminalSystem.GetBlocksOfType / GetBlocks and reuse.</param>");
            sb.AppendLine("/// <param name=\"tag\">Case-insensitive tag to search for.</param>");
            sb.AppendLine("/// <param name=\"foundName\">Receives the CustomName of the matched block, or null.</param>");
            sb.AppendLine("public IMyTextSurfaceProvider FindPanel(");
            sb.AppendLine("    List<IMyTerminalBlock> allBlocks, string tag, out string foundName)");
            sb.AppendLine("{");
            sb.AppendLine("    foundName = null;");
            sb.AppendLine("    // Pass 1: CustomName only — name always takes priority over CustomData tags");
            sb.AppendLine("    foreach (var b in allBlocks)");
            sb.AppendLine("    {");
            sb.AppendLine("        if (b is IMyTextSurfaceProvider");
            sb.AppendLine("            && b.CustomName.IndexOf(tag, StringComparison.OrdinalIgnoreCase) >= 0)");
            sb.AppendLine("        { foundName = b.CustomName; return (IMyTextSurfaceProvider)b; }");
            sb.AppendLine("    }");
            sb.AppendLine("    // Pass 2: CustomData only — tag-based discovery fallback");
            sb.AppendLine("    foreach (var b in allBlocks)");
            sb.AppendLine("    {");
            sb.AppendLine("        if (b is IMyTextSurfaceProvider");
            sb.AppendLine("            && b.CustomData.IndexOf(tag, StringComparison.OrdinalIgnoreCase) >= 0)");
            sb.AppendLine("        { foundName = b.CustomName; return (IMyTextSurfaceProvider)b; }");
            sb.AppendLine("    }");
            sb.AppendLine("    return null;");
            sb.AppendLine("}");
            sb.AppendLine();
            sb.AppendLine("// ── Live LCD Streaming (self-disarming) ──────────────────────────────");
            sb.AppendLine();
            sb.AppendLine("private NamedPipeClientStream _lcdPipe;");
            sb.AppendLine("private DateTime _lcdStreamExpiry;");
            sb.AppendLine("private bool _lcdStreamActive;");
            sb.AppendLine();
            sb.AppendLine("/// <summary>");
            sb.AppendLine("/// Starts streaming LCD snapshots to the layout tool over a named pipe.");
            sb.AppendLine("/// The stream auto-disarms after <paramref name=\"seconds\"/> (default 60).");
            sb.AppendLine("/// Call from a chat command, e.g. /lcd watch.");
            sb.AppendLine("/// The code is completely dormant when not streaming — zero overhead.");
            sb.AppendLine("/// </summary>");
            sb.AppendLine("public void StartLcdStream(int seconds = 60)");
            sb.AppendLine("{");
            sb.AppendLine("    StopLcdStream();");
            sb.AppendLine("    try");
            sb.AppendLine("    {");
            sb.AppendLine("        _lcdPipe = new NamedPipeClientStream(\".\", \"SELcdSnapshot\", PipeDirection.Out);");
            sb.AppendLine("        _lcdPipe.Connect(2000); // 2 s timeout — layout tool must be listening");
            sb.AppendLine("        _lcdStreamExpiry = DateTime.UtcNow.AddSeconds(seconds);");
            sb.AppendLine("        _lcdStreamActive = true;");
            sb.AppendLine("        Log.Info($\"LCD stream started — auto-disarms in {seconds}s\");");
            sb.AppendLine("    }");
            sb.AppendLine("    catch (Exception ex)");
            sb.AppendLine("    {");
            sb.AppendLine("        Log.Warn($\"LCD stream failed to connect: {ex.Message}\");");
            sb.AppendLine("        StopLcdStream();");
            sb.AppendLine("    }");
            sb.AppendLine("}");
            sb.AppendLine();
            sb.AppendLine("/// <summary>Stops the live LCD stream immediately.</summary>");
            sb.AppendLine("public void StopLcdStream()");
            sb.AppendLine("{");
            sb.AppendLine("    _lcdStreamActive = false;");
            sb.AppendLine("    try { _lcdPipe?.Dispose(); } catch { }");
            sb.AppendLine("    _lcdPipe = null;");
            sb.AppendLine("}");
            sb.AppendLine();
            sb.AppendLine("/// <summary>");
            sb.AppendLine("/// Sends the current snapshot frame over the pipe if streaming is active.");
            sb.AppendLine("/// Call this after SnapshotCollect() in your render loop.");
            sb.AppendLine("/// When the timer expires the stream self-disarms automatically.");
            sb.AppendLine("/// </summary>");
            sb.AppendLine("public void StreamFrame()");
            sb.AppendLine("{");
            sb.AppendLine("    if (!_lcdStreamActive) return; // dormant — zero overhead");
            sb.AppendLine();
            sb.AppendLine("    // Auto-disarm check");
            sb.AppendLine("    if (DateTime.UtcNow > _lcdStreamExpiry)");
            sb.AppendLine("    {");
            sb.AppendLine("        Log.Info(\"LCD stream expired — auto-disarmed\");");
            sb.AppendLine("        StopLcdStream();");
            sb.AppendLine("        return;");
            sb.AppendLine("    }");
            sb.AppendLine();
            sb.AppendLine("    if (_snapshotSprites.Count == 0) return;");
            sb.AppendLine();
            sb.AppendLine("    try");
            sb.AppendLine("    {");
            sb.AppendLine("        string frame = SerializeSnapshot();");
            sb.AppendLine("        byte[] payload = Encoding.UTF8.GetBytes(frame);");
            sb.AppendLine("        byte[] header = BitConverter.GetBytes(payload.Length);");
            sb.AppendLine("        _lcdPipe.Write(header, 0, 4);");
            sb.AppendLine("        _lcdPipe.Write(payload, 0, payload.Length);");
            sb.AppendLine("        _lcdPipe.Flush();");
            sb.AppendLine("    }");
            sb.AppendLine("    catch (Exception ex)");
            sb.AppendLine("    {");
            sb.AppendLine("        Log.Warn($\"LCD stream write failed: {ex.Message}\");");
            sb.AppendLine("        StopLcdStream();");
            sb.AppendLine("    }");
            sb.AppendLine("}");
            sb.AppendLine();
            sb.AppendLine("// ── File-Based Live Streaming (alternative to named pipes) ───────────");
            sb.AppendLine("// Use this when the layout tool is on the same machine as the server.");
            sb.AppendLine("// The layout tool watches the file via Edit → Watch Snapshot File…");
            sb.AppendLine();
            sb.AppendLine("private string _lcdStreamFilePath;");
            sb.AppendLine("private DateTime _lcdFileStreamExpiry;");
            sb.AppendLine("private bool _lcdFileStreamActive;");
            sb.AppendLine();
            sb.AppendLine("/// <summary>");
            sb.AppendLine("/// Starts writing LCD snapshots to a file on disk at the given path.");
            sb.AppendLine("/// The stream auto-disarms after <paramref name=\"seconds\"/> (default 60).");
            sb.AppendLine("/// Point the layout tool's \"Watch Snapshot File\" at this path.");
            sb.AppendLine("/// </summary>");
            sb.AppendLine("public void StartLcdFileStream(string filePath, int seconds = 60)");
            sb.AppendLine("{");
            sb.AppendLine("    StopLcdFileStream();");
            sb.AppendLine("    _lcdStreamFilePath = filePath;");
            sb.AppendLine("    _lcdFileStreamExpiry = DateTime.UtcNow.AddSeconds(seconds);");
            sb.AppendLine("    _lcdFileStreamActive = true;");
            sb.AppendLine("    Log.Info($\"LCD file stream started → {filePath}  (auto-disarms in {seconds}s)\");");
            sb.AppendLine("}");
            sb.AppendLine();
            sb.AppendLine("/// <summary>Stops the file-based LCD stream immediately.</summary>");
            sb.AppendLine("public void StopLcdFileStream()");
            sb.AppendLine("{");
            sb.AppendLine("    _lcdFileStreamActive = false;");
            sb.AppendLine("    _lcdStreamFilePath = null;");
            sb.AppendLine("}");
            sb.AppendLine();
            sb.AppendLine("/// <summary>");
            sb.AppendLine("/// Writes the current snapshot to the stream file if file streaming is active.");
            sb.AppendLine("/// Call this after SnapshotCollect() in your render loop.");
            sb.AppendLine("/// </summary>");
            sb.AppendLine("public void StreamFrameToFile()");
            sb.AppendLine("{");
            sb.AppendLine("    if (!_lcdFileStreamActive) return; // dormant — zero overhead");
            sb.AppendLine();
            sb.AppendLine("    if (DateTime.UtcNow > _lcdFileStreamExpiry)");
            sb.AppendLine("    {");
            sb.AppendLine("        Log.Info(\"LCD file stream expired — auto-disarmed\");");
            sb.AppendLine("        StopLcdFileStream();");
            sb.AppendLine("        return;");
            sb.AppendLine("    }");
            sb.AppendLine();
            sb.AppendLine("    if (_snapshotSprites.Count == 0) return;");
            sb.AppendLine();
            sb.AppendLine("    try");
            sb.AppendLine("    {");
            sb.AppendLine("        File.WriteAllText(_lcdStreamFilePath, SerializeSnapshot());");
            sb.AppendLine("    }");
            sb.AppendLine("    catch (Exception ex)");
            sb.AppendLine("    {");
            sb.AppendLine("        Log.Warn($\"LCD file stream write failed: {ex.Message}\");");
            sb.AppendLine("        StopLcdFileStream();");
            sb.AppendLine("    }");
            sb.AppendLine("}");
            return sb.ToString();
        }

