    /// <summary>Target code style for generated output.</summary>
    public enum CodeStyle
    {
        /// <summary>Programmable Block (ingame) script — uses Sandbox.ModAPI.Ingame.</summary>
        InGame,
        /// <summary>C# mod — uses Sandbox.ModAPI (not .Ingame) with full using directives.</summary>
        Mod,
        /// <summary>Torch/Plugin — uses Sandbox.ModAPI with full using directives and session context hints.</summary>
        Plugin,
        /// <summary>Pulsar plugin — uses VRage.Plugins.IPlugin with Init/Update/Dispose lifecycle.</summary>
        Pulsar,
    }

    public static partial class CodeGenerator
    {
        /// <summary>
        /// Generates a complete C# method body that draws every sprite in the layout
        /// onto an IMyTextSurface, ready to paste into an SE plugin, mod, or ingame script.
        /// </summary>
        public static string Generate(LcdLayout layout,
                                      CodeStyle style     = CodeStyle.InGame,
                                      string methodName   = "DrawLayout",
                                      string surfaceParam = "surface")
        {
            var sb = new StringBuilder();
            var visibleSprites = layout.Sprites.FindAll(sp => !sp.IsHidden);

            sb.AppendLine($"// ─── Generated by SE Sprite LCD Layout Tool  [{DateTime.Now:yyyy-MM-dd HH:mm}] ───");
            sb.AppendLine($"// Surface: {layout.SurfaceWidth} × {layout.SurfaceHeight}  |  Sprites: {visibleSprites.Count}");
            sb.AppendLine("//");

            switch (style)
            {
                case CodeStyle.Mod:
                    sb.AppendLine("// Required using directives:");
                    sb.AppendLine("//   using VRage.Game.GUI.TextPanel;   // SpriteType, MySprite, ContentType");
                    sb.AppendLine("//   using VRageMath;                  // Vector2, Color");
                    sb.AppendLine("//   using Sandbox.ModAPI;             // IMyTextSurface");
                    sb.AppendLine("//");
                    sb.AppendLine("// NOTE: Mod code — use IMyTextSurface from Sandbox.ModAPI (not .Ingame).");
                    break;

                case CodeStyle.Plugin:
                    sb.AppendLine("// Required using directives:");
                    sb.AppendLine("//   using VRage.Game.GUI.TextPanel;   // SpriteType, MySprite, ContentType");
                    sb.AppendLine("//   using VRageMath;                  // Vector2, Color");
                    sb.AppendLine("//   using Sandbox.ModAPI;             // IMyTextSurface");
                    sb.AppendLine("//");
                    sb.AppendLine("// NOTE: Plugin/Torch code — obtain an IMyTextSurface from a terminal block:");
                    sb.AppendLine("//   var lcd = block as IMyTextSurfaceProvider;");
                    sb.AppendLine("//   var surface = lcd?.GetSurface(0);");
                    break;

                case CodeStyle.Pulsar:
                    sb.AppendLine("// Required using directives:");
                    sb.AppendLine("//   using VRage.Game.GUI.TextPanel;   // SpriteType, MySprite, ContentType");
                    sb.AppendLine("//   using VRageMath;                  // Vector2, Color");
                    sb.AppendLine("//   using Sandbox.ModAPI;             // IMyTextPanel");
                    sb.AppendLine("//   using VRage.Plugins;              // IPlugin");
                    sb.AppendLine("//");
                    sb.AppendLine("// NOTE: Pulsar plugin — implement IPlugin with Init/Update/Dispose.");
                    sb.AppendLine("//   Find your LCD panel by scanning grids via MyEntities.GetEntities():");
                    sb.AppendLine("//   foreach (MyEntity e in MyEntities.GetEntities())");
                    sb.AppendLine("//   {");
                    sb.AppendLine("//       var grid = e as MyCubeGrid;");
                    sb.AppendLine("//       if (grid == null) continue;");
                    sb.AppendLine("//       foreach (var slim in grid.CubeBlocks)");
                    sb.AppendLine("//       {");
                    sb.AppendLine("//           var panel = slim.FatBlock as IMyTextPanel;");
                    sb.AppendLine("//           if (panel != null && panel.CustomName == \"YourLCD\")");
                    sb.AppendLine("//               surface = panel;");
                    sb.AppendLine("//       }");
                    sb.AppendLine("//   }");
                    break;

                default: // InGame / PB
                    sb.AppendLine("// Required using directives:");
                    sb.AppendLine("//   using VRage.Game.GUI.TextPanel;   // SpriteType, MySprite, ContentType");
                    sb.AppendLine("//   using VRageMath;                  // Vector2, Color");
                    sb.AppendLine("//   using Sandbox.ModAPI.Ingame;      // IMyTextSurface  (ingame scripts only)");
                    break;
            }

            sb.AppendLine("//");
            sb.AppendLine("// Sprite positions are the CENTER of each sprite in surface-local pixel coordinates.");
            sb.AppendLine("// For surfaces that do not fill the full texture (small LCDs, corner blocks) you");
            sb.AppendLine("// may need to add the viewport offset:  var ofs = (surface.TextureSize - surface.SurfaceSize) / 2f;");
            sb.AppendLine();

            string access = style == CodeStyle.InGame ? "private" : "public";
            sb.AppendLine($"{access} void {methodName}(IMyTextSurface {surfaceParam})");
            sb.AppendLine("{");
            sb.AppendLine($"    {surfaceParam}.ContentType = ContentType.SCRIPT;");
            sb.AppendLine($"    {surfaceParam}.Script = \"\";");
            sb.AppendLine();
            sb.AppendLine($"    using (var frame = {surfaceParam}.DrawFrame())");
            sb.AppendLine("    {");

            if (visibleSprites.Count == 0)
            {
                sb.AppendLine("        // No visible sprites in layout — add some in the designer!");
            }
            else
            {
                for (int i = 0; i < visibleSprites.Count; i++)
                {
                    var sp = visibleSprites[i];
                    sb.AppendLine($"        // [{i + 1}] {sp.DisplayName}");
                    sb.AppendLine("        frame.Add(new MySprite");
                    sb.AppendLine("        {");

                    if (sp.Type == SpriteEntryType.Text)
                    {
                        sb.AppendLine($"            Type           = SpriteType.TEXT,");
                        sb.AppendLine($"            Data           = {Q(sp.Text)},");
                        if (sp.IsReferenceLayout)
                            sb.AppendLine($"            // Position  = new Vector2({sp.X:F4}f, {sp.Y:F4}f),  // snapshot reference — replace with your variable");
                        else
                            sb.AppendLine($"            Position       = new Vector2({sp.X:F4}f, {sp.Y:F4}f),");
                        sb.AppendLine($"            Color          = new Color({sp.ColorR}, {sp.ColorG}, {sp.ColorB}, {sp.ColorA}),");
                        sb.AppendLine($"            FontId         = {Q(sp.FontId)},");
                        sb.AppendLine($"            Alignment      = TextAlignment.{sp.Alignment.ToString().ToUpperInvariant()},");
                        sb.AppendLine($"            RotationOrScale = {sp.Scale:F2}f,  // scale for TEXT sprites");
                    }
                    else
                    {
                        sb.AppendLine($"            Type           = SpriteType.TEXTURE,");
                        sb.AppendLine($"            Data           = {Q(sp.SpriteName)},");
                        if (sp.IsReferenceLayout)
                        {
                            sb.AppendLine($"            // Position  = new Vector2({sp.X:F4}f, {sp.Y:F4}f),  // snapshot reference");
                            sb.AppendLine($"            // Size      = new Vector2({sp.Width:F4}f, {sp.Height:F4}f),  // snapshot reference");
                        }
                        else
                        {
                            sb.AppendLine($"            Position       = new Vector2({sp.X:F4}f, {sp.Y:F4}f),");
                            sb.AppendLine($"            Size           = new Vector2({sp.Width:F4}f, {sp.Height:F4}f),");
                        }
                        sb.AppendLine($"            Color          = new Color({sp.ColorR}, {sp.ColorG}, {sp.ColorB}, {sp.ColorA}),");
                        sb.AppendLine($"            Alignment      = TextAlignment.CENTER,");
                        sb.AppendLine($"            RotationOrScale = {sp.Rotation:F4}f,  // radians");
                    }

                    sb.AppendLine("        });");

                    if (i < visibleSprites.Count - 1)
                        sb.AppendLine();
                }
            }

            sb.AppendLine("    }");
            sb.AppendLine("}");
            return sb.ToString();
        }

        private static string Q(string s)   => $"\"{Esc(s)}\"";

        /// <summary>
        /// Generates a C# helper method that the user can drop into their PB/mod/plugin.
        /// When called at runtime, it serializes every sprite currently on a surface
        /// into literal MySprite initializer code that the layout tool can import
        /// with exact positions.
        /// </summary>
        public static string GenerateSnapshotHelper(CodeStyle style = CodeStyle.InGame)
        {
            switch (style)
            {
                case CodeStyle.Pulsar:  return GenerateSnapshotHelper_Pulsar();
                case CodeStyle.Plugin:  return GenerateSnapshotHelper_Plugin();
                case CodeStyle.Mod:     return GenerateSnapshotHelper_Mod();
                default:                return GenerateSnapshotHelper_PB();
            }
        }

