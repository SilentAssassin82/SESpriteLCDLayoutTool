            sb.AppendLine("//   using System;");
            sb.AppendLine("//   using System.Collections.Generic;");
            sb.AppendLine("//   using System.IO;");
            sb.AppendLine("//   using System.IO.Pipes;");
            sb.AppendLine("//   using System.Text;");
            sb.AppendLine("//   using NLog;");
            sb.AppendLine("//   using Sandbox.Game.Entities;       // MyCubeGrid, MyEntities");
            sb.AppendLine("//   using Sandbox.ModAPI;               // IMyTextPanel");
            sb.AppendLine("//   using VRage.Game.Entity;            // MyEntity");
            sb.AppendLine("//   using VRage.Game.GUI.TextPanel;     // MySprite, SpriteType, ContentType");
            sb.AppendLine("//   using VRage.Plugins;                // IPlugin");
            sb.AppendLine("//   using VRageMath;                    // Vector2, Color");
            sb.AppendLine();
            sb.AppendLine("private static readonly Logger Log = LogManager.GetCurrentClassLogger();");
            sb.AppendLine();
            AppendSnapshotCore(sb, accessModifier: "public ");
            sb.AppendLine();
            sb.AppendLine("/// <summary>");
            sb.AppendLine("/// Snapshots all sprites currently collected and writes them to a file.");
            sb.AppendLine("/// Call SnapshotCollect(sprites) in your Update() rendering code first,");
            sb.AppendLine("/// then trigger this via a command or one-shot flag.");
            sb.AppendLine("/// </summary>");
            sb.AppendLine("public void SnapshotLcd(string label = \"LcdSnapshot\")");
            sb.AppendLine("{");
            sb.AppendLine("    if (_snapshotSprites.Count == 0)");
            sb.AppendLine("    {");
            sb.AppendLine("        Log.Warn(\"SnapshotLcd: no sprites collected — call SnapshotCollect() first\");");
            sb.AppendLine("        return;");
            sb.AppendLine("    }");
            sb.AppendLine();
            sb.AppendLine("    string output = SerializeSnapshot();");
            sb.AppendLine("    string dir = Directory.GetCurrentDirectory();");
            sb.AppendLine("    string path = Path.Combine(dir, $\"{label}_{DateTime.Now:yyyyMMdd_HHmmss}.cs\");");
            sb.AppendLine();
            sb.AppendLine("    File.WriteAllText(path, output);");
            sb.AppendLine("    Log.Info($\"LCD snapshot saved: {path}  ({_snapshotSprites.Count} sprites)\");");
            sb.AppendLine("}");
            sb.AppendLine();
            sb.AppendLine("// ── Live LCD Streaming (self-disarming) ──────────────────────────────");
            sb.AppendLine();
            sb.AppendLine("private NamedPipeClientStream _lcdPipe;");
            sb.AppendLine("private DateTime _lcdStreamExpiry;");
            sb.AppendLine("private bool _lcdStreamActive;");
            sb.AppendLine();
            sb.AppendLine("/// <summary>");
            sb.AppendLine("/// Starts streaming LCD snapshots to the layout tool over a named pipe.");
            sb.AppendLine("/// The stream auto-disarms after <paramref name=\"seconds\"/> (default 60).");
            sb.AppendLine("/// The code is completely dormant when not streaming — zero overhead.");
            sb.AppendLine("/// </summary>");
            sb.AppendLine("public void StartLcdStream(int seconds = 60)");
            sb.AppendLine("{");
            sb.AppendLine("    StopLcdStream();");
            sb.AppendLine("    try");
            sb.AppendLine("    {");
            sb.AppendLine("        _lcdPipe = new NamedPipeClientStream(\".\", \"SELcdSnapshot\", PipeDirection.Out);");
            sb.AppendLine("        _lcdPipe.Connect(2000); // 2 s timeout — layout tool must be listening");
            sb.AppendLine("        _lcdStreamExpiry = DateTime.UtcNow.AddSeconds(seconds);");
            sb.AppendLine("        _lcdStreamActive = true;");
            sb.AppendLine("        Log.Info($\"LCD stream started — auto-disarms in {seconds}s\");");
            sb.AppendLine("    }");
            sb.AppendLine("    catch (Exception ex)");
            sb.AppendLine("    {");
            sb.AppendLine("        Log.Warn($\"LCD stream failed to connect: {ex.Message}\");");
            sb.AppendLine("        StopLcdStream();");
            sb.AppendLine("    }");
            sb.AppendLine("}");
            sb.AppendLine();
            sb.AppendLine("/// <summary>Stops the live LCD stream immediately.</summary>");
            sb.AppendLine("public void StopLcdStream()");
            sb.AppendLine("{");
            sb.AppendLine("    _lcdStreamActive = false;");
            sb.AppendLine("    try { _lcdPipe?.Dispose(); } catch { }");
            sb.AppendLine("    _lcdPipe = null;");
            sb.AppendLine("}");
            sb.AppendLine();
            sb.AppendLine("/// <summary>");
            sb.AppendLine("/// Sends the current snapshot frame over the pipe if streaming is active.");
            sb.AppendLine("/// Call this after SnapshotCollect() in your Update() loop.");
            sb.AppendLine("/// When the timer expires the stream self-disarms automatically.");
            sb.AppendLine("/// </summary>");
            sb.AppendLine("public void StreamFrame()");
            sb.AppendLine("{");
            sb.AppendLine("    if (!_lcdStreamActive) return; // dormant — zero overhead");
            sb.AppendLine();
            sb.AppendLine("    // Auto-disarm check");
            sb.AppendLine("    if (DateTime.UtcNow > _lcdStreamExpiry)");
            sb.AppendLine("    {");
            sb.AppendLine("        Log.Info(\"LCD stream expired — auto-disarmed\");");
            sb.AppendLine("        StopLcdStream();");
            sb.AppendLine("        return;");
            sb.AppendLine("    }");
            sb.AppendLine();
            sb.AppendLine("    if (_snapshotSprites.Count == 0) return;");
            sb.AppendLine();
            sb.AppendLine("    try");
            sb.AppendLine("    {");
            sb.AppendLine("        string frame = SerializeSnapshot();");
            sb.AppendLine("        byte[] payload = Encoding.UTF8.GetBytes(frame);");
            sb.AppendLine("        byte[] header = BitConverter.GetBytes(payload.Length);");
            sb.AppendLine("        _lcdPipe.Write(header, 0, 4);");
            sb.AppendLine("        _lcdPipe.Write(payload, 0, payload.Length);");
            sb.AppendLine("        _lcdPipe.Flush();");
            sb.AppendLine("    }");
            sb.AppendLine("    catch (Exception ex)");
            sb.AppendLine("    {");
            sb.AppendLine("        Log.Warn($\"LCD stream write failed: {ex.Message}\");");
            sb.AppendLine("        StopLcdStream();");
            sb.AppendLine("    }");
            sb.AppendLine("}");
            sb.AppendLine();
            sb.AppendLine("// ── File-Based Live Streaming (alternative to named pipes) ───────────");
            sb.AppendLine("// Use this when the layout tool is on the same machine as the server.");
            sb.AppendLine("// The layout tool watches the file via Edit → Watch Snapshot File…");
            sb.AppendLine();
            sb.AppendLine("private string _lcdStreamFilePath;");
            sb.AppendLine("private DateTime _lcdFileStreamExpiry;");
            sb.AppendLine("private bool _lcdFileStreamActive;");
            sb.AppendLine();
            sb.AppendLine("/// <summary>");
            sb.AppendLine("/// Starts writing LCD snapshots to a file on disk at the given path.");
            sb.AppendLine("/// The stream auto-disarms after <paramref name=\"seconds\"/> (default 60).");
            sb.AppendLine("/// Point the layout tool's \"Watch Snapshot File\" at this path.");
            sb.AppendLine("/// </summary>");
            sb.AppendLine("public void StartLcdFileStream(string filePath, int seconds = 60)");
            sb.AppendLine("{");
            sb.AppendLine("    StopLcdFileStream();");
            sb.AppendLine("    _lcdStreamFilePath = filePath;");
            sb.AppendLine("    _lcdFileStreamExpiry = DateTime.UtcNow.AddSeconds(seconds);");
            sb.AppendLine("    _lcdFileStreamActive = true;");
            sb.AppendLine("    Log.Info($\"LCD file stream started → {filePath}  (auto-disarms in {seconds}s)\");");
            sb.AppendLine("}");
            sb.AppendLine();
            sb.AppendLine("/// <summary>Stops the file-based LCD stream immediately.</summary>");
            sb.AppendLine("public void StopLcdFileStream()");
            sb.AppendLine("{");
            sb.AppendLine("    _lcdFileStreamActive = false;");
            sb.AppendLine("    _lcdStreamFilePath = null;");
            sb.AppendLine("}");
            sb.AppendLine();
            sb.AppendLine("/// <summary>");
            sb.AppendLine("/// Writes the current snapshot to the stream file if file streaming is active.");
            sb.AppendLine("/// Call this after SnapshotCollect() in your Update() loop.");
            sb.AppendLine("/// </summary>");
            sb.AppendLine("public void StreamFrameToFile()");
            sb.AppendLine("{");
            sb.AppendLine("    if (!_lcdFileStreamActive) return; // dormant — zero overhead");
            sb.AppendLine();
            sb.AppendLine("    if (DateTime.UtcNow > _lcdFileStreamExpiry)");
            sb.AppendLine("    {");
            sb.AppendLine("        Log.Info(\"LCD file stream expired — auto-disarmed\");");
            sb.AppendLine("        StopLcdFileStream();");
            sb.AppendLine("        return;");
            sb.AppendLine("    }");
            sb.AppendLine();
            sb.AppendLine("    if (_snapshotSprites.Count == 0) return;");
            sb.AppendLine();
            sb.AppendLine("    try");
            sb.AppendLine("    {");
            sb.AppendLine("        File.WriteAllText(_lcdStreamFilePath, SerializeSnapshot());");
            sb.AppendLine("    }");
            sb.AppendLine("    catch (Exception ex)");
            sb.AppendLine("    {");
            sb.AppendLine("        Log.Warn($\"LCD file stream write failed: {ex.Message}\");");
            sb.AppendLine("        StopLcdFileStream();");
            sb.AppendLine("    }");
            sb.AppendLine("}");
            return sb.ToString();
        }

        /// <summary>
        /// Appends the shared SnapshotCollect + SerializeSnapshot methods.
        /// </summary>
        private static void AppendSnapshotCore(StringBuilder sb, string accessModifier)
        {
            // The snapshot collector list
            sb.AppendLine($"{accessModifier}List<MySprite> _snapshotSprites = new List<MySprite>();");
            sb.AppendLine();

            // Row data collector — captures the LcdSpriteRow[] that drove rendering
            sb.AppendLine($"{accessModifier}List<LcdSpriteRow> _snapshotRows = new List<LcdSpriteRow>();");
            sb.AppendLine();

            // Snapshot tag — set this to identify which plugin/script produced the snapshot
            sb.AppendLine("/// <summary>");
            sb.AppendLine("/// Set this to a unique label (e.g. \"MyPulsarHUD\") so the layout tool");
            sb.AppendLine("/// can distinguish snapshots from different plugins on the same LCD.");
            sb.AppendLine("/// </summary>");
            sb.AppendLine($"{accessModifier}string _snapshotTag = \"\";");
            sb.AppendLine();

            // ── Per-sprite incremental collection ────────────────────────────
            // Use SnapshotBegin() + SnapshotAdd() when sprites are built across
            // multiple render phases (e.g. text in one pass, bars/icons in another).
            // This ensures ALL sprites are captured regardless of which phase adds them.
            sb.AppendLine("/// <summary>");
            sb.AppendLine("/// Starts a new snapshot frame.  Clears previously collected sprites and rows.");
            sb.AppendLine("/// Call once at the TOP of your render method, before any SnapshotAdd calls.");
            sb.AppendLine("/// </summary>");
            sb.AppendLine($"{accessModifier}void SnapshotBegin()");
            sb.AppendLine("{");
            sb.AppendLine("    _snapshotSprites.Clear();");
            sb.AppendLine("    _snapshotRows.Clear();");
            sb.AppendLine("}");
            sb.AppendLine();

            sb.AppendLine("/// <summary>");
            sb.AppendLine("/// Adds a single sprite to the snapshot.  Call this alongside every frame.Add()");
            sb.AppendLine("/// so the snapshot captures ALL sprites — text, bars, icons, separators, etc.");
            sb.AppendLine("/// <para>Usage:  frame.Add(sprite);  SnapshotAdd(sprite);</para>");
            sb.AppendLine("/// </summary>");
            sb.AppendLine($"{accessModifier}void SnapshotAdd(MySprite sprite)");
            sb.AppendLine("{");
            sb.AppendLine("    _snapshotSprites.Add(sprite);");
            sb.AppendLine("}");
            sb.AppendLine();

            // Bulk collect sprites — for code that builds a complete list before drawing
            sb.AppendLine("/// <summary>");
            sb.AppendLine("/// Bulk-collects ALL sprites at once.  Use this when you have a single");
            sb.AppendLine("/// List&lt;MySprite&gt; that already contains every sprite (text, bars, icons).");
            sb.AppendLine("/// If your rendering adds sprites in multiple passes, prefer");
            sb.AppendLine("/// SnapshotBegin() + SnapshotAdd() instead so nothing is missed.");
            sb.AppendLine("/// </summary>");
            sb.AppendLine($"{accessModifier}void SnapshotCollect(List<MySprite> sprites)");
            sb.AppendLine("{");
            sb.AppendLine("    _snapshotSprites.Clear();");
            sb.AppendLine("    _snapshotSprites.AddRange(sprites);");
            sb.AppendLine("}");
            sb.AppendLine();

            // Collect row data — call this alongside SnapshotCollect so the layout tool
            // can use real game data instead of placeholder values.
            sb.AppendLine("/// <summary>");
            sb.AppendLine("/// Captures the LcdSpriteRow data that drove rendering so the layout");
            sb.AppendLine("/// tool can use real game values (item names, amounts, bar fills) instead");
            sb.AppendLine("/// of placeholder text.  Call this right before or after SnapshotCollect.");
            sb.AppendLine("/// </summary>");
            sb.AppendLine($"{accessModifier}void SnapshotCollectRows(LcdSpriteRow[] rows)");
            sb.AppendLine("{");
            sb.AppendLine("    _snapshotRows.Clear();");
            sb.AppendLine("    if (rows != null)");
            sb.AppendLine("        _snapshotRows.AddRange(rows);");
            sb.AppendLine("}");
            sb.AppendLine();

            // ── Debug Variables ──────────────────────────────────────────────
            // Plugin developers can stream arbitrary diagnostic state alongside
            // the sprite data so it appears in the layout tool's Variables tab.
            sb.AppendLine($"{accessModifier}Dictionary<string, object> _debugVars = new Dictionary<string, object>();");
            sb.AppendLine();

            sb.AppendLine("/// <summary>");
            sb.AppendLine("/// Records a named debug variable that will be sent to the layout tool");
            sb.AppendLine("/// alongside the sprite snapshot.  Call this for any field or computed value");
            sb.AppendLine("/// you want to inspect in the editor's Variables tab while live-streaming.");
            sb.AppendLine("/// <para>Example:  SnapshotDebug(\"_itemCount\", _itemCount);</para>");
            sb.AppendLine("/// </summary>");
            sb.AppendLine($"{accessModifier}void SnapshotDebug(string name, object value)");
            sb.AppendLine("{");
            sb.AppendLine("    _debugVars[name] = value;");
            sb.AppendLine("}");
            sb.AppendLine();

            // Serializer
            sb.AppendLine("/// <summary>");
            sb.AppendLine("/// Serializes collected sprites AND row data into a snapshot that the");
            sb.AppendLine("/// layout tool can import.  Sprites become MySprite initializer code;");
            sb.AppendLine("/// rows are appended as @ROW comments so the tool can replay them.");
            sb.AppendLine("/// Paste the output into the layout tool's \"Paste Layout\" dialog.");
            sb.AppendLine("/// </summary>");
            sb.AppendLine($"{accessModifier}string SerializeSnapshot()");
            sb.AppendLine("{");
            sb.AppendLine("    var sb = new StringBuilder();");
            sb.AppendLine("    sb.AppendLine(\"// ── LCD Snapshot ──\");");
            sb.AppendLine("    sb.AppendLine($\"// Captured: {DateTime.Now:yyyy-MM-dd HH:mm}  |  Sprites: {_snapshotSprites.Count}  |  Rows: {_snapshotRows.Count}\");");
            sb.AppendLine("    if (!string.IsNullOrEmpty(_snapshotTag))");
            sb.AppendLine("        sb.AppendLine($\"// @SnapshotTag: {_snapshotTag}\");");
            sb.AppendLine("    sb.AppendLine();");
            sb.AppendLine("    for (int i = 0; i < _snapshotSprites.Count; i++)");
            sb.AppendLine("    {");
            sb.AppendLine("        var s = _snapshotSprites[i];");
            sb.AppendLine("        sb.AppendLine(\"frame.Add(new MySprite\");");
            sb.AppendLine("        sb.AppendLine(\"{\");");
            sb.AppendLine("        sb.AppendLine($\"    Type           = SpriteType.{s.Type},\");");
            sb.AppendLine("        sb.AppendLine($\"    Data           = \\\"{s.Data}\\\",\");");
            sb.AppendLine("        if (s.Position.HasValue)");
            sb.AppendLine("            sb.AppendLine($\"    Position       = new Vector2({s.Position.Value.X:F1}f, {s.Position.Value.Y:F1}f),\");");
            sb.AppendLine("        if (s.Size.HasValue)");
            sb.AppendLine("            sb.AppendLine($\"    Size           = new Vector2({s.Size.Value.X:F1}f, {s.Size.Value.Y:F1}f),\");");
            sb.AppendLine("        if (s.Color.HasValue)");
            sb.AppendLine("            sb.AppendLine($\"    Color          = new Color({s.Color.Value.R}, {s.Color.Value.G}, {s.Color.Value.B}, {s.Color.Value.A}),\");");
            sb.AppendLine("        if (s.FontId != null)");
            sb.AppendLine("            sb.AppendLine($\"    FontId         = \\\"{s.FontId}\\\",\");");
            sb.AppendLine("        sb.AppendLine($\"    Alignment      = TextAlignment.{s.Alignment},\");");
            sb.AppendLine("        sb.AppendLine($\"    RotationOrScale = {s.RotationOrScale:F4}f,\");");
            sb.AppendLine("        sb.AppendLine(\"});\");");
            sb.AppendLine("        sb.AppendLine();");
            sb.AppendLine("    }");
            sb.AppendLine();
            // Row data section — encoded as comments so it survives paste workflows
            sb.AppendLine("    // Serialize captured row data");
            sb.AppendLine("    if (_snapshotRows.Count > 0)");
            sb.AppendLine("    {");
            sb.AppendLine("        sb.AppendLine();");
            sb.AppendLine("        sb.AppendLine(\"// ── Captured Row Data ──\");");
            sb.AppendLine("        sb.AppendLine(\"// The layout tool uses these to replay render methods with real game values.\");");
            sb.AppendLine("        sb.AppendLine(\"// Format: @ROW:Kind|Text|StatText|IconSprite|R,G,B,A|BarFill|R,G,B,A|ShowAlert\");");
            sb.AppendLine("        foreach (var r in _snapshotRows)");
            sb.AppendLine("        {");
            sb.AppendLine("            string text     = (r.Text ?? \"\").Replace(\"|\", \"\\\\|\");");
            sb.AppendLine("            string statText = (r.StatText ?? \"\").Replace(\"|\", \"\\\\|\");");
            sb.AppendLine("            string icon     = (r.IconSprite ?? \"\").Replace(\"|\", \"\\\\|\");");
            sb.AppendLine("            string tc       = $\"{r.TextColor.R},{r.TextColor.G},{r.TextColor.B},{r.TextColor.A}\";");
            sb.AppendLine("            string bf       = r.BarFill.ToString(\"F4\");");
            sb.AppendLine("            string bfc      = $\"{r.BarFillColor.R},{r.BarFillColor.G},{r.BarFillColor.B},{r.BarFillColor.A}\";");
            sb.AppendLine("            string alert    = r.ShowAlert ? \"1\" : \"0\";");
            sb.AppendLine("            sb.AppendLine($\"// @ROW:{r.RowKind}|{text}|{statText}|{icon}|{tc}|{bf}|{bfc}|{alert}\");");
            sb.AppendLine("        }");
            sb.AppendLine("    }");
            sb.AppendLine();

            // Debug variables section — encoded as comments alongside row/sprite data
            sb.AppendLine("    // Serialize debug variables");
            sb.AppendLine("    if (_debugVars.Count > 0)");
            sb.AppendLine("    {");
            sb.AppendLine("        sb.AppendLine();");
            sb.AppendLine("        sb.AppendLine(\"// ── Debug Variables ──\");");
            sb.AppendLine("        sb.AppendLine(\"// Format: @DebugVar: name (Type) = value\");");
            sb.AppendLine("        foreach (var kv in _debugVars)");
            sb.AppendLine("        {");
            sb.AppendLine("            string typeName = kv.Value != null ? kv.Value.GetType().Name : \"Null\";");
            sb.AppendLine("            string val = kv.Value != null ? kv.Value.ToString() : \"(null)\";");
            sb.AppendLine("            if (kv.Value is string) val = \"\\\"\" + val + \"\\\"\";");
            sb.AppendLine("            if (kv.Value is float f) val = f.ToString(\"F4\");");
            sb.AppendLine("            if (kv.Value is double d) val = d.ToString(\"F4\");");
            sb.AppendLine("            sb.AppendLine($\"// @DebugVar: {kv.Key} ({typeName}) = {val}\");");
            sb.AppendLine("        }");
            sb.AppendLine("    }");
            sb.AppendLine();
            sb.AppendLine("    return sb.ToString();");
            sb.AppendLine("}");
        }
        /// <summary>
        /// Escapes a string for a C# string literal, turning any PUA or non-ASCII
        /// character into a \uXXXX escape so the generated code is always ASCII-safe.
        /// </summary>
        private static string Esc(string s)
        {
            if (s == null) return "";
            var sb = new StringBuilder(s.Length * 2);
            foreach (char c in s)
            {
                if (c == '\\') { sb.Append("\\\\"); }
                else if (c == '"') { sb.Append("\\\""); }
                else if (c >= 0x007F)  // non-ASCII or PUA → \uXXXX
                    sb.Append($"\\u{(int)c:X4}");
                else
                    sb.Append(c);
            }
            return sb.ToString();
        }

